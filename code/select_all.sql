-- This would be a general select all statement
select * from tableName;

-- This would be a general way of selecting more than
-- one column from a table
select column1, column2, column3
from tableName;

-- this is another way you could select from more
-- than one column in a table
SELECT column1, column2 FROM tableName;

-- There is no right way, just whichever way is best for you
